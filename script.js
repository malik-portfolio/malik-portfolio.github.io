// Map section to data
const sectionDataMap = {
    'about': aboutData,
    'skill': skillData,
    'project': projectData
}

// Global Variables
let currentSection = 'about';

// Element Selection
const cardsWrapper = document.getElementById('cards-wrapper');
const navSection = document.getElementById('nav-section');
const cardModalWrapper = document.getElementById('card-modal-wrapper');
const cardModal = document.getElementById('card-modal');

// Event Listener
navSection.addEventListener('click', (event) => {
    document.getElementById(currentSection).classList.remove('gradient-background');
    currentSection = event.target.title;
    document.getElementById(currentSection).classList.add('gradient-background');
    renderCardCollection();
})

cardModalWrapper.addEventListener('click', () => {
    cardModalWrapper.style.display = 'none';
})

cardsWrapper.addEventListener('click', renderModal);

// Utilites
function renderCardCollection() {
    const data = sectionDataMap[currentSection];
    const cardCollection = data.map((item) => {
        const { iconName, title, description, link } = item;
        const cardHtml = generateCardHtml(iconName, title, description, link);
        return cardHtml
    }).join('');
    cardsWrapper.innerHTML = cardCollection;
}

function renderModal(event) {
    // Find Card Element
    const targetEl = event.target
    const cardEl = targetEl.closest('#card')

    // Check if card element is not rendered
    if (!cardEl) {
        return;
    }

    // If rendered than show card modal
    const description = cardEl.dataset.description;
    const link = cardEl.dataset.link;
    const iconName = cardEl.dataset.iconname
    console.log(cardEl)
    const cardModalHtml = `
        <div class="card-icon-mobile bg-${iconName}"></div>  
        <p id="card-modal-description">${description}</p>
        ${link ? `
            <div id="project-technologies">React - Tailwind CSS - TypeScript</div>  
            <a href="${link}" id="project-link" target="_blank">Visit</a>
        ` : ''}
    `;
    cardModalWrapper.style.display = 'flex';
    cardModal.innerHTML = cardModalHtml;
}

function generateCardHtml(iconName, title, description, link) {
    return `
        <div 
            class="card"
            id="card" 
            data-description="${description}" 
            data-iconName="${iconName}" 
            ${link && 'data-link="' + link + '"'}>
                <div>
                    <span class="card-icon bg-${iconName}"></span>
                </div>
                <h2 class="card-title">${title}</h2>
        </div>`;
}

// Main
renderCardCollection();